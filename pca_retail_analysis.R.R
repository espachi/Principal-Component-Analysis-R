#  3) Οι επόμενες συνιστώσες φωτίζουν αντιθέσεις (contrasts) στη δυναμική μιας  
#  οικονομίας και όψεις του πώς είναι δομημένη. Παραδείγματος χάρη, η δεύτερη  
#  συνιστώσα αποτυπώνει τη σχέση μεταξύ τουριστικής δταστηριότητςα  και εκπαιδευτικής
