# Υπολογισμός ποσοστού διακύμανσης (Variance Explained)
perc_var <- (eigR$values / sum(eigR$values)) * 100
cum_var <- cumsum(perc_var)

#Πινακάκι: Ιδιοτιμες-Ποσοστό πληροφορίας που εξηγει-Αθροιστικο ποσοστό
eigTable <- data.frame(
  Component = 1:ncol(df_num),
  Eigenvalue = eigR$values,
  Variance_Perc = perc_var,
  Cumulative_Perc = cum_var
)

print("--- Πίνακας Ιδιοτιμών & Επεξηγούμενης Διασποράς ---")
print(round(eigTable, 2))

